// plugins/with-android-share-intent.js
// Edit-only manifest plugin: adds share intent-filters to an EXISTING MainActivity.
// It will NOT create activities or mutate the manifest structure beyond adding filters.

const { withAndroidManifest, AndroidConfig } = require('@expo/config-plugins');

function getMainApplication(manifest) {
  try {
    return AndroidConfig.Manifest.getMainApplicationOrThrow(manifest);
  } catch {
    return null;
  }
}

function findMainActivity(manifest) {
  const app = getMainApplication(manifest);
  if (!app) return null;

  const activities = app.activity || [];
  // Match typical RN/Expo names
  return activities.find(a => {
    const name = a?.$?.['android:name'] || '';
    return (
      name === '.MainActivity' ||
      name.endsWith('.MainActivity') ||
      name.endsWith('MainActivity')
    );
  }) || null;
}

function ensureArray(obj, key) {
  if (!obj[key]) obj[key] = [];
  if (!Array.isArray(obj[key])) obj[key] = [obj[key]];
  return obj[key];
}

function addIntentFilter(activity, filter) {
  const filters = ensureArray(activity, 'intent-filter');

  // De-dupe by action + set of mimeTypes (order-insensitive)
  const newAction = filter.action?.[0]?.$?.['android:name'];
  const newMimes = new Set((filter.data || []).map(d => d.$['android:mimeType']));

  const exists = filters.some(f => {
    const act = f.action?.[0]?.$?.['android:name'];
    if (act !== newAction) return false;
    const mimes = new Set((f.data || []).map(d => d.$['android:mimeType']));
    if (mimes.size !== newMimes.size) return false;
    for (const mt of newMimes) if (!mimes.has(mt)) return false;
    return true;
  });

  if (!exists) filters.push(filter);
}

function makeFilter(actionName, mimeTypes) {
  return {
    action: [{ $: { 'android:name': actionName } }],
    category: [{ $: { 'android:name': 'android.intent.category.DEFAULT' } }],
    data: mimeTypes.map(mt => ({ $: { 'android:mimeType': mt } })),
  };
}

module.exports = (config) =>
  withAndroidManifest(config, (cfg) => {
    const activity = findMainActivity(cfg.modResults);
    if (!activity) {
      // If MainActivity isn't present yet in this phase, skip silently.
      // (It will exist in the final merged manifest generated by Android.)
      return cfg;
    }

    // Text/link shares
    addIntentFilter(
      activity,
      makeFilter('android.intent.action.SEND', ['text/plain', 'text/*', 'text/uri-list'])
    );

    // Single image
    addIntentFilter(
      activity,
      makeFilter('android.intent.action.SEND', ['image/*'])
    );

    // Multiple images
    addIntentFilter(
      activity,
      makeFilter('android.intent.action.SEND_MULTIPLE', ['image/*'])
    );

    return cfg;
  });

module.exports.name = 'with-android-share-intent';
